#ifndef CHIP8_H
#define CHIP8_H

#include <stdint.h>
#include <stdio.h>
#include <string.h>
#include <stdlib.h>

#define MEMORY_SIZE 4096
#define ROM_START 0x200
#define FONT_START 0x000

#define DISPLAY_W 64
#define DISPLAY_H 32
#define STACK_DEPTH 16
#define NUM_REGS 16
#define NUM_KEYS 16

static const uint8_t FONT_SET[80] = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

typedef struct
{
    uint8_t memory[MEMORY_SIZE];
    uint8_t V[NUM_REGS];
    uint16_t I;
    uint16_t pc;
    uint16_t stack[STACK_DEPTH];
    uint8_t sp;
    uint8_t delay_timer;
    uint8_t sound_timer;
    uint8_t display[DISPLAY_W * DISPLAY_H];
    uint8_t keypad[NUM_KEYS];
    uint8_t draw_flag;
} chip8_t;

void chip8_init(chip8_t *chip);
int chip8_load_rom(chip8_t *chip, const char *path);
void chip8_cycle(chip8_t *chip);
void chip8_debug_memory(const chip8_t *chip, uint16_t start, uint16_t end);
uint16_t chip8_fetch(chip8_t *chip);
void chip8_execute(chip8_t *chip, uint16_t opcode);

#endif